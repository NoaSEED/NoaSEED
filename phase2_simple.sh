    OPERATIONAL_ADDRESS=$(jq -r '.validator_info.operational_address' "$VALIDATOR_DIR/validator_config.json")
    REWARDS_ADDRESS=$(jq -r '.validator_info.rewards_address' "$VALIDATOR_DIR/validator_config.json")
